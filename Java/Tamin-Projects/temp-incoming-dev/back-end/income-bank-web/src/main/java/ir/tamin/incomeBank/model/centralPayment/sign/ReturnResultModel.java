package ir.tamin.incomeBank.model.centralPayment.sign;

/**
 *
 * @author s_maknooni
 */
public class ReturnResultModel {

    String id;
    String message;

    public ReturnResultModel(String id, String message) {
        this.id = id;
        this.message = message;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
    
    
}
